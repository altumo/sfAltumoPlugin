	/**
	 * Selects a collection of SingleSignOnKey objects pre-filled with their Session objects.
	 * @param      Criteria  $criteria
	 * @param      PropelPDO $con
	 * @param      String    $join_behavior the type of joins to use, defaults to Criteria::LEFT_JOIN
	 * @return     array Array of SingleSignOnKey objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinSession(Criteria $criteria, $con = null, $join_behavior = Criteria::LEFT_JOIN)
	{
		$criteria = clone $criteria;

		// Set the correct dbName if it has not been overridden
		if ($criteria->getDbName() == Propel::getDefaultDB()) {
			$criteria->setDbName(self::DATABASE_NAME);
		}

		SingleSignOnKeyPeer::addSelectColumns($criteria);
		$startcol = SingleSignOnKeyPeer::NUM_HYDRATE_COLUMNS;
		SessionPeer::addSelectColumns($criteria);

		$criteria->addJoin(SingleSignOnKeyPeer::SESSION_ID, SessionPeer::ID, $join_behavior);

		// symfony_behaviors behavior
		foreach (sfMixer::getCallables(self::getMixerPreSelectHook(__FUNCTION__)) as $sf_hook)
		{
		  call_user_func($sf_hook, 'BaseSingleSignOnKeyPeer', $criteria, $con);
		}

		$stmt = BasePeer::doSelect($criteria, $con);
		$results = array();

		while ($row = $stmt->fetch(PDO::FETCH_NUM)) {
			$key1 = SingleSignOnKeyPeer::getPrimaryKeyHashFromRow($row, 0);
			if (null !== ($obj1 = SingleSignOnKeyPeer::getInstanceFromPool($key1))) {
				// We no longer rehydrate the object, since this can cause data loss.
				// See http://www.propelorm.org/ticket/509
				// $obj1->hydrate($row, 0, true); // rehydrate
			} else {

				$cls = SingleSignOnKeyPeer::getOMClass(false);

				$obj1 = new $cls();
				$obj1->hydrate($row);
				SingleSignOnKeyPeer::addInstanceToPool($obj1, $key1);
			} // if $obj1 already loaded

			$key2 = SessionPeer::getPrimaryKeyHashFromRow($row, $startcol);
			if ($key2 !== null) {
				$obj2 = SessionPeer::getInstanceFromPool($key2);
				if (!$obj2) {

					$cls = SessionPeer::getOMClass(false);

					$obj2 = new $cls();
					$obj2->hydrate($row, $startcol);
					SessionPeer::addInstanceToPool($obj2, $key2);
				} // if obj2 already loaded

				// Add the $obj1 (SingleSignOnKey) to $obj2 (Session)
				$obj2->addSingleSignOnKey($obj1);

			} // if joined row was not null

			$results[] = $obj1;
		}
		$stmt->closeCursor();
		return $results;
	}


